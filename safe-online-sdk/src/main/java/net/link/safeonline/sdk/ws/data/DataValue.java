package net.link.safeonline.sdk.ws.data;

public class DataValue {

	private final String name;

	private final String value;

	public DataValue(String name, String value) {
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return this.name;
	}

	public String getValue() {
		return this.value;
	}
}
