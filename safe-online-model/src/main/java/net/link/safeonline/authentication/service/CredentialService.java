package net.link.safeonline.authentication.service;

import java.net.MalformedURLException;

import javax.ejb.Local;

import net.link.safeonline.authentication.exception.ArgumentIntegrityException;
import net.link.safeonline.authentication.exception.AttributeTypeNotFoundException;
import net.link.safeonline.authentication.exception.DeviceNotFoundException;
import net.link.safeonline.authentication.exception.LastDeviceException;
import net.link.safeonline.authentication.exception.MobileException;
import net.link.safeonline.authentication.exception.MobileRegistrationException;
import net.link.safeonline.authentication.exception.PermissionDeniedException;
import net.link.safeonline.pkix.exception.TrustDomainNotFoundException;

/**
 * Interface of service that manages the credentials of the caller subject.
 * 
 * @author fcorneli
 * 
 */
@Local
public interface CredentialService {

	/**
	 * Changes the password of the current user. Of course for that to happen
	 * the oldPassword must match.
	 * 
	 * @param oldPassword
	 * @param newPassword
	 * @throws PermissionDeniedException
	 */
	void changePassword(String oldPassword, String newPassword)
			throws PermissionDeniedException, DeviceNotFoundException;

	/**
	 * Removes the password of the current user. For this to happen the password
	 * must match.
	 * 
	 * @param password
	 * @throws DeviceNotFoundException
	 * @throws PermissionDeniedException
	 * @throws LastDeviceException
	 */
	void removePassword(String password) throws DeviceNotFoundException,
			PermissionDeniedException, LastDeviceException;

	/**
	 * Registers a mobile for the current user.
	 * 
	 * @param mobile
	 * @return activation code to be used on your mobile
	 * @throws ArgumentIntegrityException
	 * @throws MobileRegistrationException
	 * @throws MalformedURLException
	 * @throws MobileException
	 */
	String registerMobile(String mobile) throws MobileException,
			MalformedURLException, MobileRegistrationException,
			ArgumentIntegrityException;

	/**
	 * Removes a mobile for the current user.
	 * 
	 * @param mobile
	 * @throws MobileException
	 * @throws MalformedURLException
	 * @throws LastDeviceException
	 */
	void removeMobile(String mobile) throws MobileException,
			MalformedURLException, LastDeviceException;

	/**
	 * Merges the identity statement with the current user subject. The identity
	 * statement has been generated by the client-side web application component
	 * and is cryptographically signed by the user's authentication certificate.
	 * 
	 * @param identityStatementData
	 *            the identity statement.
	 * @throws TrustDomainNotFoundException
	 * @throws PermissionDeniedException
	 *             in case the identity statement user does not correspond with
	 *             the active principal.
	 * @throws ArgumentIntegrityException
	 *             in case the identity statement didn't pass the integrity
	 *             checks.
	 * @throws AttributeTypeNotFoundException
	 */
	void mergeIdentityStatement(byte[] identityStatementData)
			throws TrustDomainNotFoundException, PermissionDeniedException,
			ArgumentIntegrityException, AttributeTypeNotFoundException;

	/**
	 * Gives back <code>true</code> if the user already has a password
	 * configured.
	 * 
	 * @return
	 */
	boolean isPasswordConfigured();

	/**
	 * Remove the identity statement information from the current user subject.
	 * 
	 * @param identityStatementData
	 * @throws TrustDomainNotFoundException
	 * @throws PermissionDeniedException
	 * @throws ArgumentIntegrityException
	 * @throws AttributeTypeNotFoundException
	 */
	void removeIdentity(byte[] identityStatementData)
			throws TrustDomainNotFoundException, PermissionDeniedException,
			ArgumentIntegrityException, AttributeTypeNotFoundException;
}
